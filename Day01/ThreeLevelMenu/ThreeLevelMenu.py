print("按键提醒：\n‘r’表示返回上一级菜单；\n‘q’表示退出程序；")
listMenu=[]
while True:
    for key in data:
        print(key)
    choice = input("请选择进入：").strip()
    if choice in data:
        # 当前菜单数据存储起来，方便返回上一级使用
        listMenu.append(data)
        # 获取选择菜单的数据项
        data=data[choice]
    elif choice == "r":
        # 当列表空的时候，就是退出大循环的时候
        if len(listMenu) == 0:
            break
        # 获取上一级菜单的数据项
        data=listMenu[-1]
        # 删除列表最后一个元素，
        listMenu.pop()
    elif choice == "q":
        sys.exit()
    else:
        print("输入错误，请您重新输入！")
